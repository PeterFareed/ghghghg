client.on("message", async message => {
  if(message.author.bot) return;
  if(message.channel.type === "dm") return;

  let prefix = "#";
  let messageArray = message.content.split (" ");
  let cmd = messageArray[0];
  let args = messageArray.slice(1);



    if(cmd === `${prefix};كيك`){



      let kUser = message.guild.member(message.mentions.users.first() || message.guild.members.get(args[0]));
      if(!kUser) return message.channel.send("فين العضو ؟");
      let kReason = args.join(" ").slice(22);
      if(!message.member.hasPermission("MANAGE_CHANNELS")) return message.channel.send("ما عندك برمشن");
      if(kUser.hasPermission("MANAGE_CHANNELS")) return message.channel.send("ما تقدر تسوي كيك للأدمين")

      let kickEmbed = new Discord.RichEmbed()
      .setDescription("~Kick~")
      .setColor("#e56b00")
      .addField("الذي طرد", `${kUser} with ID ${kUser.id}`)
      .addField("طرد بواسطه", `<@${message.author.id}> with the id ${message.author.id}`)
      .addField("طرد في", message.channel)
      .addField("الوقت", message.createdAt)
      .addField("السبب", kReason);

      let kickChannel = message.guild.channels.find('name', 'chat');
      if(!kickChannel) return message.channel.send("لم اجد روم chat");

      message.guild.member(kUser).kick(kReason)
      kickChannel.send(kickEmbed);
    }
    });

